    private void jbCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCadastrarActionPerformed
        // TODO add your handling code here:
        if ((jtfCod.getText().equals("")) || (jtfNome.getText().equals("")) || (jtfRua.getText().equals("")) || (jtfNumero.getText().equals("")) || (jtfCidade.getText().equals("")) || (jtfEstado.getText().equals("")) || (jtfTelefone.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!!");
        } else {
            int cod = Integer.parseInt(jtfCod.getText());
            String nome = jtfNome.getText();
            String rua = jtfRua.getText();
            int numero = Integer.parseInt(jtfNumero.getText());
            String cidade = jtfCidade.getText();
            String estado = jtfEstado.getText();
            String telefone = jtfTelefone.getText();

            int r = JOptionPane.showConfirmDialog(null, "Confira e confirme se os dados estão corretos:"
                    + "\nCódigo: " + cod
                    + "\nNome: " + nome
                    + "\nRua: " + rua
                    + "\nNúmero: " + numero
                    + "\nCidade: " + cidade
                    + "\nEstado: " + estado
                    + "\nTelefone" + telefone, "Confirmação", 0, 1);
            if (r == 0) {
                Cliente cliente = new Cliente();
                cliente.setCod(cod);
                cliente.setNome(nome);
                cliente.setRua(rua);
                cliente.setNumero(numero);
                cliente.setCidade(cidade);
                cliente.setEstado(estado);
                cliente.setTelefone(telefone);
                DAO dao = new DAO();
                if (dao.incluirCliente(cliente)) {
                    JOptionPane.showMessageDialog(null, "Cliente Cadastrado com Sucesso!!!");
                    listarCliente();
                } else {
                    JOptionPane.showMessageDialog(null, "Cliente não pode ser Cadastrado!!!");
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jbCadastrarActionPerformed

