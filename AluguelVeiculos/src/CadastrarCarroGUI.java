    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        if ((jtfCod.getText().equals("")) || (jtfModelo.getText().equals("")) || (jtfAno.getText().equals("")) || (jtfPlaca.getText().equals("")) || (jtfCor.getText().equals("")) || (jtfLugares.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos!!");
        } else {
            String modelo = jtfModelo.getText();
            int ano = Integer.parseInt(jtfAno.getText());
            String placa = jtfPlaca.getText();
            String cor = jtfCor.getText();
            String lugar = jtfLugares.getText();
            int cod = Integer.parseInt(jtfCod.getText());

            int r = JOptionPane.showConfirmDialog(null, "Confira e confirme se os dados estão corretos:"
                    + "\nCódigo: " + cod
                    + "\nModelo: " + modelo
                    + "\nAno: " + ano
                    + "\nPlaca: " + placa
                    + "\nCor: " + cor
                    + "\nLugares: " + lugar, "Confirmação", 0, 1);
            if (r == 0) {
                Veiculo veiculo = new Veiculo();
                veiculo.setCod(cod);
                veiculo.setModelo(modelo);
                veiculo.setAno(ano);
                veiculo.setPlaca(placa);
                veiculo.setCor(cor);
                veiculo.setLugar(lugar);
                DAO dao = new DAO();
                if (dao.alterarVeiculo(veiculo, cod)) {
                    JOptionPane.showMessageDialog(null, "Cadastro alterado com Sucesso!!!");
                    listarVeiculo();
                } else {
                    JOptionPane.showMessageDialog(null, "O Cadastro não pode ser Alterado!!!");
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

